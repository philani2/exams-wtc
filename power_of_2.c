#include <stdio.h>

int		power_of_2(unsigned int n)
{
	if (n > 0)
		while ((n % 2) == 0)
			n /= 2;
	return ((n == 1) ? 1 : 0);
}

int main()
{
	int i;
	i = 0;
	i = power_of_2(7);
	printf("%d\n", i);
	return (0);
}
